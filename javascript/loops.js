/**
 * loops - while loop
 */
start = 1
end = 10
console.log("print 1's table")
while (start <=end){
    console.log(start)
    start = start +1
}
console.log("print 1's table in reverse order")
start = 10
end = 1
while(end<=start){
    console.log(start)
    start-=1
}
/**
 * for loop
 */
console.log('for loop')
for (let i=1;i<=10;i++){
    console.log(i)
}